# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

print(f'\n{"-"*5} Задание-3 {"-"*5}')

import os
import random
import re


def open_files (path):
    with open(path, 'w', encoding='UTF-8') as f:
        number_list = [random.randint(0, 9) for _ in range(2500)]
        number_str = ''.join(str(number_list[i]) for i in number_list)
        f.write(number_str)


def max_count_num (path):
    with open(path, 'r', encoding='UTF-8') as f:
        source_number = f.readline()
        count_per_num = []
        for i in range(0, 9):
            if str(i) in source_number:
                pattern_3 = f'[{i}]+'
                count_per_num.append(max(re.findall(pattern_3, source_number), key=len))
        return max(count_per_num, key=len)


path = os.path.join('2500_number.txt')
open_files(path)
print(max_count_num(path))

